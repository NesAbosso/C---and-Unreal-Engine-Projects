// Variables.cpp : This file contains the 'main' function. Program execution begins and ends there.
//

#include <iostream>
#include <string>

int main()
{
    // Declaring a char variable
    char c = 'A';
    std::cout << "char: " << c << std::endl;

    // Declaring an int variable
    int i = 42;
    std::cout << "int: " << i << std::endl;

    // Declaring a double variable
    double d = 3.14;
    std::cout << "double: " << d << std::endl;

    // Declaring a bool variable
    bool b = true;
    std::cout << "bool: " << b << std::endl;

    // Declaring a string variable
    std::string s = "Hello, World!";
    std::cout << "string: " << s << std::endl;

    // Declaring multiple variables of the same data type
    int x = 1, y = 2, z = 3;
    std::cout << "x: " << x << " y: " << y << " z: " << z << std::endl;

    return 0; std::cout << "Hello World!\n";
}

